package com.example.junit_test

// Mendefinisikan kelas Calculator
class Calculator {
     // Mendefinisikan metode untuk penjumlahan
    fun add(first: Int, second: Int): Int {
        // Melakukan penjumlahan antara 'second' dan 'first', 
        // dan mengembalikan hasilnya
        return second + first
    }
     // Mendefinisikan metode untuk pengurangan
    fun sub(first: Int, second: Int): Int {
            return first - second
    }
}
